export async function seedModelQuestionsChemistry() {
  for (const qq of QUESTIONS) {
    // 检查是否已存在相同的题（按 nodeId + title 去重）
    const existing = await prisma.examQuestion.findFirst({
      where: { nodeId: qq.nodeId, title: qq.title },
    });
    if (existing) {
      await prisma.examQuestion.update({
        where: { id: existing.id },
        data: qq,
      });
    } else {
      await prisma.examQuestion.create({ data: qq });
    }
  }
  console.log(`  ✓ 化学模型专题：${QUESTIONS.length} 道题目`);
}

// 独立运行：npx tsx src/seeders/model-questions-chemistry.ts
const isMain = process.argv[1] && process.argv[1].endsWith('model-questions-chemistry.ts');
if (isMain) {
  seedModelQuestionsChemistry()
    .then(() => { console.log('✅ 化学模型专题题目完成'); process.exit(0); })
    .catch(e => { console.error('❌', e); process.exit(1); });
}
