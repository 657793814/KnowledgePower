// =====================================================================
// 导出函数
// =====================================================================
export async function seedModelQuestionsMath() {
  for (const qq of QUESTIONS) {
    const existing = await prisma.examQuestion.findFirst({
      where: { nodeId: qq.nodeId, title: qq.title },
    });
    if (existing) {
      await prisma.examQuestion.update({
        where: { id: existing.id },
        data: qq,
      });
    } else {
      await prisma.examQuestion.create({ data: qq });
    }
  }
  console.log(`  ✓ ${QUESTIONS.length} 道数学模型专题题目`);
}

// ===== 独立运行 =====
const isMain = process.argv[1] && process.argv[1].endsWith('model-questions-math.ts');
if (isMain) {
  seedModelQuestionsMath()
    .then(() => { console.log('✅ 完成'); process.exit(0); })
    .catch(e => { console.error('❌', e); process.exit(1); });
}
