export async function seedModelQuestionsPhysics() {
  for (const qq of QUESTIONS) {
    const existing = await prisma.examQuestion.findFirst({
      where: { nodeId: qq.nodeId, title: qq.title },
    });
    if (existing) {
      await prisma.examQuestion.update({ where: { id: existing.id }, data: qq });
    } else {
      await prisma.examQuestion.create({ data: qq });
    }
  }
  console.log(`  ✓ ${QUESTIONS.length} 道物理模型专题题目`);
}

const isMain = process.argv[1] && process.argv[1].endsWith("model-questions-physics.ts");
if (isMain) {
  seedModelQuestionsPhysics()
    .then(() => { console.log("✅ 完成"); process.exit(0); })
    .catch(e => { console.error("❌", e); process.exit(1); });
}
